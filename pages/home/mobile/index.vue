<template>
  <div id="page-home-mobile">
    <a-row type="flex" justify="center">
      hola
      <a-avatar :src="this.$store.state.userPicture" size="large" />
      {{ this.$store.state.userId }}
    </a-row>
    <a-button @click="toMatches">
      Matches
    </a-button>
    <a-button @click="findMatch">
      Encontrar partida
    </a-button>
    <a-button @click="logOut">
      Cerrar
    </a-button>
  </div>
</template>

<script>
export default {
  name: 'HomeMobile',
  methods: {
    toMatches () {
      this.$router.push({
        name: process.env.routes.match.name
      })
    },
    findMatch () {
      const apiName = process.env.aws.APIGATEWAY_UMATCH_NAME
      const path = process.env.aws.LAMBDA_ARV_UMT_SEARCH_GEOLOCATIONS
      const params = {
        body: {
          hashKey: this.$store.state.userGeohash,
          lastKey: null
        }
      }

      this.$Amplify.API.post(apiName, path, params).then((response) => {
        console.log(response)
      }).catch((error) => {
        console.log(error)
      })
    },
    logOut () {
      this.$Amplify.Auth.signOut({ global: true })
        .then((data) => {
          this.$store.commit('resetStates')
          console.log(data)
        })
        .catch(err => console.log(err))
    }
  }
}
</script>

